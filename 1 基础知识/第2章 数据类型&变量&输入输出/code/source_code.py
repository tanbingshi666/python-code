# 拆包
x1, x2, *x3 = 10, 20, 30, 45, 56, 100, 7, 88
print(x1, x2, x3)  # 10 20 [30, 45, 56, 100, 7, 88]

x1, *x2, x3 = 10, 20, 30, 45, 56, 100, 7, 88
print(x1, x2, x3)  # 10 [20, 30, 45, 56, 100, 7] 88

*x1, x2, x3 = 10, 20, 30, 45, 56, 100, 7, 88
print(x1, x2, x3)  # [10, 20, 30, 45, 56, 100] 7 88

# 打包
n1, n2, n3 = [4, 5, 67]
print(n1, n2, n3)  # 4 5 67


def test(*num):
    print(num)  # (345, 6, 7, 7, 8, 8, 914, 6, 75, 7)


test(345, 6, 7, 7, 8, 8, 914, 6, 75, 7)
